/**
 * Represents the current state of keyboard keys relevant for game controls.
 */
class Keyboard {
    /** @type {boolean} True if the left key is pressed */
    LEFT = false;
    /** @type {boolean} True if the right key is pressed */
    RIGHT = false;
    /** @type {boolean} True if the up key is pressed */
    UP = false;
    /** @type {boolean} True if the down key is pressed */
    DOWN = false;
    /** @type {boolean} True if the space key is pressed */
    SPACE = false;
    /** @type {boolean} True if the D key is pressed */
    D = false;
}
